# по номеру месяца выводим его название

month = int(input("Ввелите номер месяца (1-12):"))

if month < 1 or month > 12:
    print("Неверный номер")
else:
    names = ["Январь",
             "Февраль",
             "Март",
             "Апрель",
             "Май",
             "Июнь",
             "Июль",
             "Август",
             "Сентябрь",
             "Октябрь",
             "Ноябрь",
             "Декабрь"]
    
    print("Месяц:", names[month-1])

